import streamlit as st  # Python 코드로 웹 화면을 만들기 위해 Streamlit을 st라는 별칭으로 가져옵니다.

st.title("학습 피드백 앱")  # Streamlit 화면의 가장 큰 제목을 표시합니다.

name = st.text_input("이름")  # 계산 결과나 입력값을 이후 코드에서 다시 쓰기 위해 변수에 저장합니다.
score = st.number_input("오늘의 이해도 점수", min_value=0, max_value=100, value=70)  # 계산 결과나 입력값을 이후 코드에서 다시 쓰기 위해 변수에 저장합니다.
comment = st.text_area("오늘 배운 내용 한 줄 정리")  # 계산 결과나 입력값을 이후 코드에서 다시 쓰기 위해 변수에 저장합니다.

if score >= 80:  # 조건식이 True일 때만 아래 들여쓰기 블록을 실행합니다.
    message = "좋습니다. 다음 실습으로 넘어갈 준비가 되어 있습니다."  # 계산 결과나 입력값을 이후 코드에서 다시 쓰기 위해 변수에 저장합니다.
elif score >= 50:  # 앞 조건이 False일 때 추가 조건을 검사합니다.
    message = "기본 흐름은 이해했습니다. 예제를 한 번 더 수정해 보세요."  # 계산 결과나 입력값을 이후 코드에서 다시 쓰기 위해 변수에 저장합니다.
else:  # 위 조건들이 모두 False일 때 실행할 대체 흐름입니다.
    message = "천천히 괜찮습니다. 첫 번째 예제부터 다시 실행해 보세요."  # 계산 결과나 입력값을 이후 코드에서 다시 쓰기 위해 변수에 저장합니다.

if name and comment:  # 조건식이 True일 때만 아래 들여쓰기 블록을 실행합니다.
    st.success(f"{name}님의 피드백")  # 작업이 성공했음을 초록색 성공 메시지로 표시합니다.
    st.write(message)  # 문자열, 숫자, 객체를 Streamlit 화면에 출력합니다.
    st.caption(f"오늘의 정리: {comment}")  # 보조 설명이나 현재 설정값을 작은 글씨로 표시합니다.
else:  # 위 조건들이 모두 False일 때 실행할 대체 흐름입니다.
    st.warning("이름과 오늘 배운 내용 한 줄 정리를 입력하세요.")  # 주의가 필요한 상황을 경고 메시지로 표시합니다.

