"""함수 기초 예제입니다.

함수는 자주 사용하는 코드를 이름으로 묶어 두는 문법입니다.
함수를 사용하면 같은 코드를 여러 번 복사하지 않고, 필요한 곳에서 호출할 수 있습니다.

이 파일에서는 세 가지 함수를 만듭니다.

1. 이름을 받아 인사 문장을 만드는 함수
2. 두 숫자를 더하는 함수
3. 점수를 받아 합격 여부를 판단하는 함수
"""


def greet(name: str) -> str:
    """이름을 받아 인사 문장을 반환합니다.

    매개변수:
        name: 인사할 사람의 이름입니다.

    반환값:
        완성된 인사 문장입니다.
    """

    # return은 함수의 결과를 함수 밖으로 돌려줍니다.
    # 여기서는 f-string으로 이름이 포함된 문장을 만듭니다.
    return f"{name}님, 안녕하세요."


def add(a: int, b: int) -> int:
    """두 숫자를 더한 결과를 반환합니다.

    매개변수:
        a: 첫 번째 숫자입니다.
        b: 두 번째 숫자입니다.

    반환값:
        a와 b를 더한 정수입니다.
    """

    return a + b


def is_passed(score: int) -> bool:
    """점수가 60점 이상이면 True를 반환합니다.

    매개변수:
        score: 검사할 점수입니다.

    반환값:
        60점 이상이면 True, 그렇지 않으면 False입니다.
    """

    # 비교식의 결과는 True 또는 False입니다.
    return score >= 60


# Python은 위에서 아래로 실행되므로, 함수를 먼저 정의한 뒤 아래에서 호출합니다.
# greet("Jean")은 name 매개변수에 "Jean"을 넣어 함수를 실행합니다.
print(greet("Jean"))

# add(3, 5)는 a에 3, b에 5를 넣고 두 값을 더합니다.
print("3 + 5 =", add(3, 5))

# is_passed(75)는 75점이 합격 기준인 60점 이상인지 확인합니다.
print("합격 여부:", is_passed(75))
