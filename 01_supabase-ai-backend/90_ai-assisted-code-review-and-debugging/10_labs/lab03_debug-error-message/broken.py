scores = ["90", "80", "70"]  # 계산 결과나 입력값을 이후 코드에서 다시 쓰기 위해 변수에 저장합니다.

total = 0  # 계산 결과나 입력값을 이후 코드에서 다시 쓰기 위해 변수에 저장합니다.

for score in scores:  # 목록이나 반복 가능한 데이터를 하나씩 꺼내 같은 작업을 반복합니다.
    total += score  # 이 줄은 예제의 핵심 동작을 단계별로 보여주기 위한 코드입니다.

average = total / len(scores)  # 계산 결과나 입력값을 이후 코드에서 다시 쓰기 위해 변수에 저장합니다.
print("Average:", average)  # 터미널에 값을 출력해 코드 실행 결과를 확인합니다.

